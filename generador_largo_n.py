# generar arrays de numeros aleatorios sin repeteicion de tama単o pasado por parametro maximo
# y generar un archivo con el array generado hasta el tama単o pasado por parametro
import random
import sys
from typing import List
import os


def generar_array(size: int, element_size: int = 1000) -> List[int]:
    array = random.sample(range(1, element_size), size)

    os.makedirs("tests_cases/autogenerated", exist_ok=True)

    with open(f"tests_cases/autogenerated/test{size}.txt", "w") as file:
        file.write(";".join(str(i) for i in array))
    return array


if __name__ == "__main__":
    if len(sys.argv) != 3:
        print(
            "Argumentos incorrectos.\nUso: python3 generador.py <tama単o max arreglo> <valor maximo de moneda>"
        )
    max_len = int(sys.argv[1])
    max_coin = int(sys.argv[2])

    if max_coin < max_len:
        print(
            "El valor maximo de las monedas debe ser mayor o igual al tama単o maximo del arreglo a generar"
        )

    generar_array(max_len, max_coin)
